public class num3 {
    public static void main(String[] args){
	/* declares both variables as double */
	double number1;
	double number2;
	double sum;
	double twiceTheSum;
	double product;
	double twiceTheProduct;
	double quotient;
	double twiceTheQuotient;
	/* prints the two numbers */
	number1 = 5.4;
	number2 = 6.7;
	System.out.println("number 1:" +number1);
	System.out.println("number 2:" +number2);
	/* calculates the sum of two numbers */
	sum = number1 + number2;
	/* prints the sum */
	System.out.println("sum:" +sum);
	/* calculates the twice the sum of the numbers*/
	twiceTheSum = 2*sum;
	/* calculates the product of the numbers */
	product = number1 * number2;
	/* caluclates twice the products of the numbers */
	twiceTheProduct = 2*product;
	/* calculates the quotient of the numbers */
	quotient = number1 / number2;
	/* calculates twice the quotient of the numbers */
	twiceTheQuotient = 2*quotient;

	/* prints the sum */
	System.out.println("sum:" +sum);
	/* prints out twice the sum */
	System.out.println("twice the sum:" +twiceTheSum);

	/* prints out product */
	System.out.println("product:" +product);
	/* prints out twice the product */
	System.out.println("twice the product:" +twiceTheProduct);

	/* prints out quotient */
	System.out.println("Quotient:" +quotient);
	/* prints out twice the quotient */
	System.out.println("Twice the quotien:t" +twiceTheQuotient);
    }
}
	 
